 private void showDetails(){
        dm1=(DefaultTableModel)jTable1.getModel();
        String url = "jdbc:sqlite:car_sale.db";
        String sql="select * from delete_records";
        PreparedStatement pstmt;
        DecimalFormat df = new DecimalFormat("#.##");  
        try {
            con= DriverManager.getConnection(url);
            pstmt=con.prepareStatement(sql);
            ResultSet rss = pstmt.executeQuery();
                        while(rss.next()){
                            
                            start=rss.getDouble("start_meter");
                            end=rss.getDouble("end_meter");
                            kilometers=end-start;
                     
                            dm1.addRow(new Object[]{rss.getString("vehicle_number"),rss.getString("vehicle_brand")
                            ,rss.getString("owner_name"),rss.getInt("owner_id"),rss.getString("owner_contact")
                            ,rss.getDouble("owner_address"),df.format(rss.getString("owner_payment")),df.format(rss.getString("cost"))
                            ,df.format(rss.getString("cost_kilometer")),rss.getString("borrowed_date"),rss.getDouble("returned_date"),
                            });
                        }
        }catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null,"Error Loading");
        }
        
    try {
        con.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null,"Error Closing database");
    }
        
    }
